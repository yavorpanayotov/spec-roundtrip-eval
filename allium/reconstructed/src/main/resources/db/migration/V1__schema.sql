create table app_user (
    id bigint generated by default as identity primary key,
    name varchar(255) not null,
    email varchar(255) not null,
    role varchar(20) not null
);

create table expense_claim (
    id bigint generated by default as identity primary key,
    owner_id bigint not null,
    title varchar(255) not null,
    status varchar(20) not null,
    created_at timestamp not null,
    submitted_at timestamp,
    decided_at timestamp,
    reimbursed_at timestamp,
    decision_reason varchar(2000),
    constraint fk_claim_owner foreign key (owner_id) references app_user (id)
);

create table expense_item (
    id bigint generated by default as identity primary key,
    claim_id bigint not null,
    category varchar(20) not null,
    description varchar(255) not null,
    amount decimal(12, 2) not null,
    expense_date date not null,
    has_receipt boolean not null,
    constraint fk_item_claim foreign key (claim_id) references expense_claim (id) on delete cascade,
    constraint chk_item_amount_positive check (amount > 0)
);

create table decision_log_entry (
    id bigint generated by default as identity primary key,
    claim_id bigint not null,
    actor_id bigint not null,
    action varchar(20) not null,
    occurred_at timestamp not null,
    reason varchar(2000),
    constraint fk_decision_claim foreign key (claim_id) references expense_claim (id) on delete cascade,
    constraint fk_decision_actor foreign key (actor_id) references app_user (id)
);

CREATE TABLE app_user (
    id          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name        VARCHAR(100) NOT NULL,
    email       VARCHAR(255) NOT NULL UNIQUE,
    role        VARCHAR(20)  NOT NULL CHECK (role IN ('EMPLOYEE', 'MANAGER', 'FINANCE'))
);

CREATE TABLE expense_claim (
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    owner_id        BIGINT       NOT NULL REFERENCES app_user (id),
    title           VARCHAR(200) NOT NULL,
    status          VARCHAR(20)  NOT NULL DEFAULT 'DRAFT'
                    CHECK (status IN ('DRAFT', 'SUBMITTED', 'APPROVED', 'REJECTED', 'REIMBURSED')),
    created_at      TIMESTAMP    NOT NULL,
    submitted_at    TIMESTAMP,
    decided_at      TIMESTAMP,
    reimbursed_at   TIMESTAMP,
    decision_reason VARCHAR(1000)
);

CREATE TABLE expense_item (
    id           BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    claim_id     BIGINT        NOT NULL REFERENCES expense_claim (id) ON DELETE CASCADE,
    category     VARCHAR(20)   NOT NULL
                 CHECK (category IN ('TRAVEL', 'MEALS', 'ACCOMMODATION', 'EQUIPMENT', 'OTHER')),
    description  VARCHAR(500)  NOT NULL,
    amount       DECIMAL(12, 2) NOT NULL CHECK (amount > 0),
    expense_date DATE          NOT NULL,
    has_receipt  BOOLEAN       NOT NULL DEFAULT FALSE
);

CREATE TABLE decision_log (
    id          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    claim_id    BIGINT      NOT NULL REFERENCES expense_claim (id) ON DELETE CASCADE,
    actor_id    BIGINT      NOT NULL REFERENCES app_user (id),
    action      VARCHAR(20) NOT NULL
                CHECK (action IN ('SUBMITTED', 'WITHDRAWN', 'APPROVED', 'REJECTED', 'REIMBURSED')),
    occurred_at TIMESTAMP   NOT NULL,
    reason      VARCHAR(1000)
);

CREATE INDEX idx_claim_owner ON expense_claim (owner_id);
CREATE INDEX idx_claim_status ON expense_claim (status);
CREATE INDEX idx_item_claim ON expense_item (claim_id);
CREATE INDEX idx_log_claim ON decision_log (claim_id);
